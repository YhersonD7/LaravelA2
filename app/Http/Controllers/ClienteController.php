    {
        $cliente = new Cliente;

        $cliente->id = $request->id;
        $cliente->nombre = $request->nombre;
        $cliente->direccion = $request->direccion;
        $cliente->celular = $request->celular;
        $cliente->save();
        return view('clientes.create');



